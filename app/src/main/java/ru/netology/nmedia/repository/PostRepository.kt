    private val mutableLiveData = MutableLiveData<List<Post>>(posts)
    override val listLiveData: LiveData<List<Post>>
        get() = mutableLiveData

    override fun likeById(id: Long) {
        mutableLiveData.value = mutableLiveData.value?.map {
            if (it.id == id) {
                var likes = if (it.likedByMe) it.likes.inc() else if (it.likes > 0) it.likes.dec() else 0
                it.copy(likedByMe = !it.likedByMe, likes = likes)
            } else it
        }
    }

    override fun shareById(id: Long) {
        mutableLiveData.value = mutableLiveData.value?.map{
            if (it.id == id) {
                it.copy(shares = it.shares.inc())
            } else it
        }
    }

    override fun removeById(id: Long) {
        mutableLiveData.value = mutableLiveData.value?.filter { it.id != id  }
    }

    override fun create(post: Post) {
        posts = posts
                .orEmpty()
                .toMutableList()
                .apply {
                    add(post.copy(id = ids++)) }
        mutableLiveData.value = posts
    }

    override fun update(post: Post) {
        posts = posts
                .orEmpty()
                .map {
                    if (it.id == post.id) it.copy(content = post.content) else it }

        mutableLiveData.value = posts
    }

    override fun save(post: Post) {
        posts = posts.filter { it.id != post.id }
    }



}